
def areaTriangle(base,height):
      '''
      objective: To Compute area of a Triangle 
      input parameters 
      length : base if the Triangle
      breadth: height of the Triangle
      approach: multiply base and height and 1/2
      return value : Area of the Triangle
      '''
      area = (1/2) * base * height
      return area
def areaRectangle(length,breadth):
      '''
      objective: To Compute area of a rectangle 
      input parameters 
      length : length if the rectangle
      breadth: breadth of the rectangle
      approach: multiply length and breadth of the rectangle
      return value : Area of the rectangle
      '''
      area = length * breadth
      return area
def areaSquare(side):
      '''
      objective: To Compute area of a rectangle 
      input parameters 
      side : side of the rectangle
      
      approach: multiply side with side
      return value : Area of the square
      '''

      area= side * side
      return area
    


    
